// Define the book object
const book = {
    title: "To Kill a Mockingbird",
    author: "Harper Lee",
    year: 1960
};

// Function to print book properties
function printBookInfo(book) {
    console.log(`Title: [${book.title}], Author: [${book.author}], Year: [${book.year}]`);
}

// Example usage:
printBookInfo(book);
// Output: Title: [To Kill a Mockingbird], Author: [Harper Lee], Year: [1960]
